class Solution
{
  public:
  
    bool check(Node* root,Node* s){
        if(s==NULL) return true;
        else if(root && s->data==root->data){
            if((root->left && s->left==NULL) || (root->right && s->right==NULL)) return false;
            return check(root->left,s->left) && check(root->right,s->right);
        }else return false;
    }
    //Function to check if S is a subtree of tree T.
    bool isSubTree(Node* root, Node* s) 
    {
        // Your code here
        if(root==NULL) return false;
        else if(root->data==s->data){
            bool t = check(root,s);
            if(t) return true;
            else {
                goto k;
            }
        }else{
        k:    int l=-1,r=-1;
            l = isSubTree(root->left,s);
            r=isSubTree(root->right,s);
            if(l==1 || r==1) return true;
            return false;
        }
    }
};

